def my_function():
    for i in range(1, 20):
        if i == 20:
            print("You got it")


my_function()

# 1. What is the for loop doing?
# It counts from 1 up to, but not including, 20.

# 2. When is the function meant to print "You got it"?
# When the value of i becomes 20.

# 3. What are your assumptions about the value of i?
# I assumed that i would reach 20, but it actually doesn't.